                                    <div class="timeline-heading">
                                        <h4>1997-2008</h4>
                                        <h4 class="subheading">St. Jean New Warriors GM</h4>
                                    </div>
                                    <div class="timeline-body">
                                        <p class="text-muted"> St. Jean had become the new Warriors' GM in July 1997; he and his predecessor Dave Twardzik received much of the blame for the Warriors' struggles early in Cohan's turbulent tenure as owner in addition to Cohan himself. St. Jean brought in players such as Terry Cummings, John Starks and Mookie Blaylock who were well past their primes. Twardzik drafted several flops, such as Todd Fuller (while Kobe Bryant was still available as well as Steve Nash and Jermaine O'Neal) and Steve Logan (who never played an NBA game). In the following draft, the team selected Adonal Foyle while Tracy McGrady was still available. St. Jean did, however, draft future two-time NBA slam dunk champion off-guard Jason Richardson (from Michigan State), a Warriors' star scorer through the 2006–07 season.

                                            For a few years, with rising stars Richardson, small forward Antawn Jamison and point guard Gilbert Arenas leading the team, the Warriors seemed like a team on the rise; but the young Warriors did not have enough in the competitive Western Conference to make the playoffs. </p>
