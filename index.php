                                    <div class="timeline-heading">
                                        <h4>1987–1997</h4>
                                        <h4 class="subheading">Resurgence Under Coach Karl</h4>
                                    </div>
                                    <div class="timeline-body">
                                        <p class = "text-muted"> After a subpar stretch in the late 1970s and early 1980s, the team had a brief resurgence under coach Karl, culminating in a 1987 Western Conference Semifinal match against Magic Johnson and the Los Angeles Lakers which is still shown on TV in the NBA's Greatest Games series. In the game, Warriors' All-Star point guard Sleepy Floyd's performance in the second half still stands as the NBA playoff record for points scored in a quarter (29) and in a half (39).
                                            The "Sleepy Floyd game" was a catalyst for increased interest in the NBA in the Bay Area which was furthered by new coach Don Nelson, who engineered another successful string of wins in the late 1980s and early 1990s with the high-scoring trio of point guard Tim Hardaway, guard Mitch Richmond and forward Chris Mullin (collectively known as "Run TMC" after the rap group Run-D.M.C.). </p>
