                                    <div class="timeline-heading">
                                        <h4>2011-Present</h4>
                                        <h4 class="subheading">Splash Brothers Championship</h4>
                                    </div>
                                    <div class="timeline-body">
                                        <p class="muted-text"> The Warriors did not improve in the 2011–12 NBA season under coach Jackson, finishing the lockout-shorted season with a 23–43 record, 13th in the conference. The team suffered several injuries to key players, and due to the lockout, Jackson could not establish his system in training camp. They then entered into another chaotic rebuilding phase.
                                            The team finished the 2012-2013 season with a record of 47–35, earning the sixth seed in the Western Conference, and defeated the Denver Nuggets in the first round of the playoffs by winning four out of six games. They lost in the second round to the San Antonio Spurs, four games to two. This was the first playoff experience for all of the starters of this group except for Andrew Bogut.
                                            The Warriors completed the 2014-2015 regular season with a record of 67-15, the best in the league and the most wins in franchise history. The Warriors also finished with a home record of 39–2, the second best in NBA history. They were first in defensive efficiency for the season and second in offensive efficiency, barely missing the mark that the Julius Erving led Sixers achieved by being first in both offensive and defensive efficiency. On May 4, Stephen Curry was named the 2014–15 NBA Most Valuable Player, the first Warrior since Wilt Chamberlain to do so. In the first round of the playoffs, they swept the New Orleans Pelicans in the first round, then defeated Memphis Grizzlies in the second round and defeated Houston Rockets in the Western Conference Finals. The Warriors advanced to their first NBA Finals since 1975, where they defeated the Cleveland Cavaliers in six games to win their fourth NBA title, and their first in 40 years. Andre Iguodala was named Finals MVP.</p>
