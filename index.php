                                    <div class="timeline-heading">
                                        <h4>1971-1978</h4>
                                        <h4 class="subheading">Golden State Warriors and Championship</h4>
                                    </div>
                                    <div class="timeline-body">
                                        <p class="text-muted">The Warriors changed their name to the Golden State Warriors for the 1971–72 season, playing almost all home games in Oakland. The Warriors made the playoffs from 1971 to 1977 (except 1974) and won their first championship on the West Coast in 1974–75. In what many consider the biggest upset in the history of the NBA, the Warriors defeated the heavily favored Washington Bullets in a four-game sweep.</p>
